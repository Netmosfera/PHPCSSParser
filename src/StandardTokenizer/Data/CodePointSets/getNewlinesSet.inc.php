use Netmosfera\PHPCSSAST\StandardTokenizer\Data\CompressedCodePointSet;
use function Netmosfera\PHPCSSAST\StandardTokenizer\Data\cp;

//[][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][][]

function getNewlinesSet(){
    $set = new CompressedCodePointSet();
    $set->add(cp("\n"));
    $set->add(cp("\r"));
    $set->add(cp("\f"));
    return $set;
}
